number_input = int(input())

if number_input == 1:
    print('Monday')
elif number_input == 2:
    print('Tuesday')
elif number_input == 3:
    print('Wednesday')
elif number_input == 4:
    print('Thursday')
elif number_input == 5:
    print('Friday')
elif number_input == 6:
    print('Saturday')
elif number_input == 7:
    print('Sunday')
else:
    print('Error')